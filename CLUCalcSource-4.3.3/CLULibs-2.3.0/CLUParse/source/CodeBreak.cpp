{

}


//////////////////////////////////////////////////////////////////////
/// Apply

bool CCodeBreak::Apply(CCodeBase* pCodeBase, SCodeData *pData)
{
	if (!pCodeBase)
		return false;

	pCodeBase->m_ErrorList.Break();
	return false;
}

